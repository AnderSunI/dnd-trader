def guess_category(name):
    for key, val in category_map.items():
        if key in name:
            return val
    return "Чудесный предмет"

def guess_rarity_from_price(price_str):
    """По строке цены (например '501-5 000 зм', 'от 50 001 зм') определить редкость"""
    if not price_str:
        return None
    price_str = price_str.replace(" ", "").lower()
    # Диапазоны по умолчанию из DMG (ориентировочно)
    if "от50001" in price_str or "50001" in price_str:
        return "легендарный"
    if "5001-50000" in price_str:
        return "очень редкий"
    if "501-5000" in price_str:
        return "редкий"
    if "101-500" in price_str:
        return "необычный"
    if "50-100" in price_str:
        return "обычный"
    # Если есть слово "от" и большое число
    if "от" in price_str:
        import re
        nums = re.findall(r'\d+', price_str)
        if nums and int(nums[0]) > 5000:
            return "очень редкий"
        if nums and int(nums[0]) > 500:
            return "редкий"
    return None

def guess_rarity_from_description(desc):
    desc_low = desc.lower()
    if "легендарный" in desc_low or "артефакт" in desc_low:
        return "легендарный"
    if "очень редкий" in desc_low:
        return "очень редкий"
    if "редкий" in desc_low:
        return "редкий"
    if "необычный" in desc_low:
        return "необычный"
    return "обычный"

def is_clean_category(cat):
    """Проверяет, что категория выглядит как настоящая, а не как мусор."""
    if not cat:
        return False
    if len(cat) > 30:
        return False
    if cat in category_map.values():
        return True
    # Дополнительно разрешим некоторые стандартные категории
    if cat in ["Оружие", "Доспех", "Щит", "Шлем", "Обувь", "Перчатки", "Наручи", "Пояс", "Плащ", "Мантия", "Амулет", "Кольцо", "Жезл", "Посох", "Палочка", "Свиток", "Зелье", "Масло", "Чудесный предмет", "Брошь", "Татуировка", "Инструмент", "Боеприпас", "Метательное", "Одежда", "Протез"]:
        return True
    return False

def is_clean_rarity(rar):
    if not rar:
        return False
    return rar in ["обычный", "необычный", "редкий", "очень редкий", "легендарный", "артефакт"]

def main():
    if not Path(INPUT_FILE).exists():
        print(f"Файл {INPUT_FILE} не найден!")
        return

    with open(INPUT_FILE, 'r', encoding='utf-8') as f:
        items = json.load(f)

    print(f"Загружено {len(items)} предметов.")

    # Сначала применим ручные исправления
    fix_dict = {name: (cat, rar) for name, cat, rar in fixes}
    for item in items:
        name = item["name"]
        if name in fix_dict:
            item["category"], item["rarity"] = fix_dict[name]
            continue

        # Если категория грязная, угадаем по названию
        if not is_clean_category(item.get("category", "")):
            new_cat = guess_category(name)
            item["category"] = new_cat

        # Если редкость грязная, угадаем
        if not is_clean_rarity(item.get("rarity", "")):
            # Сначала по цене
            price = item.get("price", "")
            new_rar = guess_rarity_from_price(price)
            if not new_rar:
                new_rar = guess_rarity_from_description(item.get("description", ""))
            item["rarity"] = new_rar if new_rar else "обычный"

    # Сохраняем результат
    with open(OUTPUT_FILE, 'w', encoding='utf-8') as f:
        json.dump(items, f, ensure_ascii=False, indent=2)

    print(f"Готово. Сохранено в {OUTPUT_FILE}. Всего предметов: {len(items)}.")

if __name__ == "__main__":
    main()
