def compact_text(value: Any) -> str:
    return " ".join(str(value or "").split())


def source_label(source_key: str) -> str:
    return SOURCE_REFS.get(source_key, {}).get("label", source_key)


def build_entry(item: Dict[str, Any]) -> Dict[str, Any]:
    title = item["ru_name"]
    en_name = item["en_name"]
    summary = compact_text(item["summary"])
    why = [compact_text(x) for x in item.get("why_it_matters", []) if compact_text(x)]
    use = [compact_text(x) for x in item.get("use_at_table", []) if compact_text(x)]
    factions = item.get("factions") or []
    threats = item.get("threats") or []
    related = item.get("related_locations") or []
    source_keys = item.get("source_keys") or []

    full_description = []
    full_description.append(summary)
    if why:
        full_description.append("Почему важно: " + " ".join(why))
    if use:
        full_description.append("Как использовать за столом: " + " ".join(use))
    if factions:
        full_description.append("Связанные силы: " + ", ".join(factions) + ".")
    if threats:
        full_description.append("Типичные угрозы: " + ", ".join(threats) + ".")
    if related:
        full_description.append("Связанные места: " + ", ".join(related) + ".")

    return {
        "id": item["id"],
        "category": "locations",
        "title": title,
        "subtitle": f"{item['location_type']} • {item['region']} • {en_name}",
        "tags": item.get("tags", []),
        "source": "; ".join(source_label(k) for k in source_keys[:3]) or "Forgotten Realms / D&D 5e",
        "source_url": "",
        "summary": summary,
        "body": [summary, *(why[:2])],
        "full_description": full_description,
        "related": related + factions[:4],
        "player_visible": item.get("visibility") != "gm_only",
        "gm_only": item.get("visibility") == "gm_only",
        "info_panels": [
            {"label": "EN", "value": en_name},
            {"label": "Тип", "value": item["location_type"]},
            {"label": "Сеттинг", "value": item["setting"]},
            {"label": "Регион", "value": item["region"]},
            {"label": "Статус", "value": item.get("review_status", "round1")},
        ],
        "mechanics": {
            "short_rules": [],
            "examples": use,
        },
        "location_data": {
            "slug": item["slug"],
            "ru_name": title,
            "en_name": en_name,
            "location_type": item["location_type"],
            "setting": item["setting"],
            "region": item["region"],
            "source_keys": source_keys,
            "campaign_refs": item.get("campaign_refs", []),
            "factions": factions,
            "threats": threats,
            "related_locations": related,
            "map_layer": "locations_round1",
            "map_status": "needs_coordinates",
            "visibility": item.get("visibility", "player_safe"),
        },
        "review_status": item.get("review_status", "round1_curated_needs_source_review"),
        "raw_fields": None,
    }


def build_map_hooks(items: List[Dict[str, Any]]) -> Dict[str, Any]:
    hooks = []
    for item in items:
        hooks.append(
            {
                "id": item["id"],
                "ru_name": item["ru_name"],
                "en_name": item["en_name"],
                "location_type": item["location_type"],
                "setting": item["setting"],
                "region": item["region"],
                "related_locations": item.get("related_locations", []),
                "factions": item.get("factions", []),
                "threats": item.get("threats", []),
                "map_status": "needs_coordinates",
                "suggested_marker_type": suggested_marker_type(item["location_type"]),
                "visibility": item.get("visibility", "player_safe"),
            }
        )
    return {
        "entity_type": "location_map_hooks",
        "project": "D&D Trader",
        "dataset": "locations_map_hooks_round1",
        "generated_at": now_iso(),
        "items": hooks,
    }


def suggested_marker_type(location_type: str) -> str:
    t = location_type.lower()
    if "город" in t or "столица" in t or "посёл" in t:
        return "settlement"
    if "подзем" in t or "гробница" in t or "руины" in t or "замок" in t:
        return "dungeon"
    if "лес" in t or "пустын" in t or "джунг" in t or "долина" in t:
        return "wilderness"
    if "море" in t or "порт" in t or "океан" in t:
        return "water"
    if "план" in t or "домен" in t:
        return "planar"
    return "location"


def write_json(path: Path, payload: Dict[str, Any]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(json.dumps(payload, ensure_ascii=False, indent=2), encoding="utf-8")


def write_report(path: Path, entries: List[Dict[str, Any]], items: List[Dict[str, Any]]) -> None:
    by_type: Dict[str, int] = {}
    by_setting: Dict[str, int] = {}
    for item in items:
        by_type[item["location_type"]] = by_type.get(item["location_type"], 0) + 1
        by_setting[item["setting"]] = by_setting.get(item["setting"], 0) + 1

    lines = [
        "D&D Trader — Locations round1 papyrus report",
        f"generated_at: {now_iso()}",
        f"locations_total: {len(entries)}",
        "",
        "By setting:",
        *[f"- {k}: {v}" for k, v in sorted(by_setting.items())],
        "",
        "By type:",
        *[f"- {k}: {v}" for k, v in sorted(by_type.items())],
        "",
        "Locations:",
    ]
    for entry in entries:
        data = entry.get("location_data", {})
        lines.append(f"- {entry['title']} [{data.get('en_name')}] :: {data.get('location_type')} / {data.get('region')}")

    lines.extend(
        [
            "",
            "Notes:",
            "- Это curated round1, не финальная канонизация.",
            "- Координаты карты не проставлены: map_status=needs_coordinates.",
            "- GM-sensitive места не удалены, а помечены, чтобы потом фронт мог скрывать спойлеры.",
            "- Следующий clean-pass: разделить settlement/region/dungeon/planar, добавить координаты, parent-child связи и источники по каждой карточке.",
        ]
    )
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text("\n".join(lines) + "\n", encoding="utf-8")


def try_copy_preview(preview_path: Path) -> str:
    try:
        FRONTEND_PREVIEW.parent.mkdir(parents=True, exist_ok=True)
        FRONTEND_PREVIEW.write_text(preview_path.read_text(encoding="utf-8"), encoding="utf-8")
        return f"[OK] frontend: copied -> {FRONTEND_PREVIEW}"
    except Exception as exc:
        return f"[WARN] frontend copy skipped: {exc}"


def main() -> int:
    entries = [build_entry(item) for item in LOCATIONS]

    master = {
        "entity_type": "location_collection",
        "project": "D&D Trader",
        "dataset": "locations_master_round1_papyrus",
        "scope": "D&D 5e14-era curated locations, primarily Forgotten Realms plus selected 5e campaign/planar locations",
        "generated_at": now_iso(),
        "source_refs": SOURCE_REFS,
        "items": LOCATIONS,
        "preview_entry_count": len(entries),
    }
    preview = {
        "entity_type": "location_collection",
        "project": "D&D Trader",
        "dataset": "locations_bestiari_preview_round1_papyrus",
        "generated_at": now_iso(),
        "entries": entries,
        "source_refs": SOURCE_REFS,
    }

    master_path = OUT_DIR / "locations_master_round1.json"
    preview_path = OUT_DIR / "locations_bestiari_preview.json"
    hooks_path = OUT_DIR / "locations_map_hooks_round1.json"
    report_path = OUT_DIR / "locations_round1_report.txt"

    write_json(master_path, master)
    write_json(preview_path, preview)
    write_json(hooks_path, build_map_hooks(LOCATIONS))
    write_report(report_path, entries, LOCATIONS)

    print(f"[OK] locations: {len(entries)}")
    print(f"[OK] master: {master_path}")
    print(f"[OK] preview: {preview_path}")
    print(f"[OK] map_hooks: {hooks_path}")
    print(f"[OK] report: {report_path}")
    print(try_copy_preview(preview_path))
    return 0


